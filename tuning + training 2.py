def get_images_by_class(split):
    img_dir = SPLITS_DIR / split / 'images'
    lbl_dir = SPLITS_DIR / split / 'labels'

    by_class = {i: [] for i in range(5)}
    empty = []

    for lbl in lbl_dir.glob('*.txt'):
        content = lbl.read_text().strip()
        img = img_dir / (lbl.stem + '.jpg')
        if not img.exists():
            continue
        if not content:
            empty.append((img, lbl))
        else:
            classes = set(int(l.split()[0]) for l in content.split('\n') if l.strip())
            primary = min(classes)
            by_class[primary].append((img, lbl))

