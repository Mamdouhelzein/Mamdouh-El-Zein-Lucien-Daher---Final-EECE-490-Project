import shutil
import random
from pathlib import Path

random.seed(42)

BALANCED_DIR = Path('/content/balanced')
CLASS_NAMES = {0: 'deer', 1: 'boar', 2: 'rabbit', 3: 'jackal', 4: 'pheasant'}

train_img = BALANCED_DIR / 'train' / 'images'
train_lbl = BALANCED_DIR / 'train' / 'labels'
test_img  = BALANCED_DIR / 'test' / 'images'
test_lbl  = BALANCED_DIR / 'test' / 'labels'

TARGET = 100  # target per class in test

def get_class_images(img_dir, lbl_dir, target_class):
    result = []
    for lbl in lbl_dir.glob('*.txt'):
        content = lbl.read_text().strip()
        if not content:
            continue
        classes = [int(l.split()[0]) for l in content.split('\n') if l.strip()]
        if target_class in classes:
            img = img_dir / (lbl.stem + '.jpg')
            if img.exists():
                result.append((img, lbl))
    return result

def count_class(lbl_dir, target_class):
    count = 0
    for lbl in lbl_dir.glob('*.txt'):
        content = lbl.read_text().strip()
        if content and any(int(l.split()[0]) == target_class
                          for l in content.split('\n') if l.strip()):
            count += 1
    return count

# Move images from train to test for underrepresented classes
for cid in range(5):
    species = CLASS_NAMES[cid]
    current = count_class(test_lbl, cid)
    needed = TARGET - current

    if needed <= 0:
        print(f"{species}: already at {current}, skipping")
        continue

    train_imgs = get_class_images(train_img, train_lbl, cid)
    to_move = random.sample(train_imgs, min(needed, len(train_imgs)))

    for img, lbl in to_move:
        shutil.move(str(img), test_img / img.name)
        shutil.move(str(lbl), test_lbl / lbl.name)

    print(f"✓ {species}: {current} → {current + len(to_move)}")

# Move empty frames from train to test
empty_in_train = []
for lbl in train_lbl.glob('*.txt'):
    if lbl.stat().st_size == 0:
        img = train_img / (lbl.stem + '.jpg')
        if img.exists():
            empty_in_train.append((img, lbl))

current_empty = sum(1 for l in test_lbl.glob('*.txt') if l.stat().st_size == 0)
needed_empty = 40 - current_empty

if needed_empty > 0:
    to_move = random.sample(empty_in_train, min(needed_empty, len(empty_in_train)))
    for img, lbl in to_move:
        shutil.move(str(img), test_img / img.name)
        shutil.move(str(lbl), test_lbl / lbl.name)
    print(f"✓ empty: {current_empty} → {current_empty + len(to_move)}")

print("\n✓ Test set boosted!")

