TARGET = 1200

def augment_image(img):
    aug = img.copy()
    choice = random.randint(0, 3)
    if choice == 0:
        factor = random.uniform(0.6, 1.4)
        aug = np.clip(aug * factor, 0, 255).astype(np.uint8)
    elif choice == 1:
        mean = np.mean(aug)
        factor = random.uniform(0.7, 1.3)
        aug = np.clip((aug - mean) * factor + mean, 0, 255).astype(np.uint8)
    elif choice == 2:
        noise = np.random.normal(0, 10, aug.shape).astype(np.int16)
        aug = np.clip(aug.astype(np.int16) + noise, 0, 255).astype(np.uint8)
    elif choice == 3:
        h, w = aug.shape[:2]
        angle = random.uniform(-10, 10)
        M = cv2.getRotationMatrix2D((w//2, h//2), angle, 1.0)
        aug = cv2.warpAffine(aug, M, (w, h))
    return aug

def flip_labels(lbl_path):
    content = lbl_path.read_text().strip()
    if not content:
        return content
    lines = []
    for line in content.split('\n'):
        if not line.strip():
            continue
        parts = line.split()
        parts[1] = str(round(1 - float(parts[1]), 6))
        lines.append(' '.join(parts))
    return '\n'.join(lines)

def get_class_images(target_class):
    result = []
    for lbl in dst_lbl.glob('*.txt'):
        content = lbl.read_text().strip()
        if not content:
            continue
        classes = [int(l.split()[0]) for l in content.split('\n') if l.strip()]
        if target_class in classes:
            img = dst_img / (lbl.stem + '.jpg')
            if img.exists():
                result.append((img, lbl))
    return result

# Augment rabbit, jackal, pheasant
for target_class in [2, 3, 4]:
    species = CLASS_NAMES[target_class]
    existing = get_class_images(target_class)
    current = len(existing)
    needed = TARGET - current

    if needed <= 0:
        print(f"{species}: already at {current}, no augmentation needed")
        continue

    print(f"\n{species}: {current} images, generating {needed} more...")
    aug_count = 0

    while aug_count < needed:
        src_img_path, src_lbl_path = random.choice(existing)
        img = cv2.imread(str(src_img_path))
        if img is None:
            continue

        # Random augmentation
        aug = augment_image(img)

        # Random horizontal flip
        flip = random.random() > 0.5
        if flip:
            aug = cv2.flip(aug, 1)
            lbl_content = flip_labels(src_lbl_path)
        else:
            lbl_content = src_lbl_path.read_text().strip()

        new_name = f"aug_{species}_{aug_count}.jpg"
        cv2.imwrite(str(dst_img / new_name), aug)
        (dst_lbl / f"aug_{species}_{aug_count}.txt").write_text(lbl_content)

        aug_count += 1

    print(f"  ✓ {species}: {current} → {current + aug_count}")

print("\n✓ Augmentation complete!")

