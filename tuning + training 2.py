TARGET = 80

def get_images_by_class(target_class):
    result = []
    for lbl in test_lbl.glob('*.txt'):
        content = lbl.read_text().strip()
        if not content:
            continue
        classes = [int(l.split()[0]) for l in content.split('\n') if l.strip()]
        if target_class in classes:
            img = test_img / (lbl.stem + '.jpg')
            if img.exists():
                result.append((img, lbl))
