from ultralytics import YOLO
import matplotlib.pyplot as plt
from pathlib import Path
import random

model = YOLO('/content/drive/MyDrive/ForestLens/runs/forest_lens_v2/weights/best.pt')

test_images = list(Path('/content/balanced/test/images').glob('*.jpg'))
random.shuffle(test_images)

for i, img_path in enumerate(test_images[:20]):
    results = model.predict(str(img_path), conf=0.35, verbose=False)
    result = results[0]

    if len(result.boxes) == 0:
        continue

    detections = [(model.names[int(b.cls[0])], f"{float(b.conf[0]):.0%}")
                  for b in result.boxes]

    plt.figure(figsize=(8, 6))
    plt.imshow(result.plot()[:,:,::-1])
    plt.axis('off')
    plt.title(str(detections))
    plt.show()

    print(f"Image {i+1}: {detections}")

