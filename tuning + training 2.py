import cv2
import numpy as np
from pathlib import Path
from tqdm import tqdm

BALANCED_DIR = Path('/content/balanced')
img_dir = BALANCED_DIR / 'train' / 'images'

images = list(img_dir.glob('*.jpg'))
print(f"Normalizing {len(images)} images...")

for img_path in tqdm(images):
    img = cv2.imread(str(img_path))
    if img is None:
        continue

    # Convert to LAB color space
    lab = cv2.cvtColor(img, cv2.COLOR_BGR2LAB)
    l, a, b = cv2.split(lab)

    # Apply CLAHE to L channel only (brightness normalization)
    clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8, 8))
    l = clahe.apply(l)

    # Merge and convert back
    lab = cv2.merge([l, a, b])
    normalized = cv2.cvtColor(lab, cv2.COLOR_LAB2BGR)

    cv2.imwrite(str(img_path), normalized)

print("✓ Homogeneity normalization complete!")

