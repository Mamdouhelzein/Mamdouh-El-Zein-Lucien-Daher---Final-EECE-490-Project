import random
from pathlib import Path
from collections import Counter

random.seed(42)

BALANCED_DIR = Path('/content/balanced')
train_img = BALANCED_DIR / 'train' / 'images'
train_lbl = BALANCED_DIR / 'train' / 'labels'

# Separate single-boar and multi-boar images
single_boar = []
multi_boar = []

for lbl in train_lbl.glob('*.txt'):
    content = lbl.read_text().strip()
    if not content:
        continue
    lines = [l for l in content.split('\n') if l.strip()]
    classes = [int(l.split()[0]) for l in lines]

    if 1 not in classes:  # not a boar image
        continue

    boar_count = classes.count(1)
    img = train_img / (lbl.stem + '.jpg')
    if not img.exists():
        continue

    if boar_count == 1:
        single_boar.append((img, lbl))
    else:
        multi_boar.append((img, lbl))

print(f"Single boar images: {len(single_boar)}")
print(f"Multi boar images:  {len(multi_boar)}")
print(f"Total boar images:  {len(single_boar) + len(multi_boar)}")

# Keep all single boar, remove most multi boar
# Target: ~1,300 total boar annotations
to_keep_multi = random.sample(multi_boar, min(100, len(multi_boar)))
to_remove = [x for x in multi_boar if x not in to_keep_multi]

print(f"\nRemoving {len(to_remove)} multi-boar images...")
for img, lbl in to_remove:
    img.unlink()
    lbl.unlink()

print(f"✓ Done. Kept {len(single_boar) + len(to_keep_multi)} boar images")

